public class Example9
{
public static void main(String[] args)
{
int x,y;
x = 20;
y = (x==1) ? 61:90;
System.out.println("Value of y when the condition is false is: " + y);
y = (x==20) ? 61:90;
System.out.println("Value of y when the condition is true is: " + y); 
}
}
/* This example uses the ternary operator to return different values of y depending on the true/false nature of the condition imposed.*/
