public class Example7
{
public static void main(String[] args)
{
double myDouble = 9.78d;
System.out.println("This is a double variable");
System.out.println(myDouble);
int myInt = (int)myDouble; //Manual or Explicit casting of double to int.
System.out.println("Type casted to int");
System.out.println(myInt);
}
}
/* This example creates an double variable and explicitly type casts it to double.*/
